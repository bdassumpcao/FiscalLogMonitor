CREATE TABLE SERVER_LIFECYCLE_EVENTS (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    EVENT_TIME TIMESTAMP NOT NULL,
    EVENT_TYPE VARCHAR(32) NOT NULL,
    HTTP_PORT INTEGER,
    HTTPS_PORT INTEGER,
    MESSAGE BLOB SUB_TYPE TEXT,
    SOURCE_FILE VARCHAR(1024),
    FINGERPRINT VARCHAR(64) NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UNIQUE (FINGERPRINT)
);

CREATE INDEX IDX_SLE_EVENT_TIME ON SERVER_LIFECYCLE_EVENTS (EVENT_TIME);
CREATE INDEX IDX_SLE_EVENT_TYPE ON SERVER_LIFECYCLE_EVENTS (EVENT_TYPE);
