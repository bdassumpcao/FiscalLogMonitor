CREATE TABLE LOG_FILES (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    FILE_NAME VARCHAR(260) NOT NULL,
    FILE_PATH VARCHAR(1024) NOT NULL,
    FILE_HASH VARCHAR(64),
    LAST_WRITE_TS TIMESTAMP,
    PROCESSED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UNIQUE (FILE_PATH, LAST_WRITE_TS)
);

CREATE TABLE LOG_ENTRIES (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    LOG_FILE_ID BIGINT,
    EXCEPTION_MESSAGE BLOB SUB_TYPE TEXT,
    EXCEPTION_CLASS VARCHAR(128),
    EXCEPTION_TIME TIMESTAMP,
    EXCEPTION_ADDRESS VARCHAR(64),
    APPLICATION_NAME VARCHAR(128),
    APPLICATION_VERSION VARCHAR(64),
    COMPUTER_NAME VARCHAR(128),
    SESSION_ID VARCHAR(128),
    THREAD_ID INTEGER,
    ACTIVE_FORM VARCHAR(255),
    CALLBACK_NAME VARCHAR(255),
    CLIENT_IP VARCHAR(64),
    REQUEST_PATH VARCHAR(255),
    REQUEST_METHOD VARCHAR(16),
    SEVERITY VARCHAR(16),
    ERROR_CATEGORY VARCHAR(32),
    STACKTRACE BLOB SUB_TYPE TEXT,
    RAW_TEXT BLOB SUB_TYPE TEXT,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT FK_LOG_ENTRIES_FILE FOREIGN KEY (LOG_FILE_ID) REFERENCES LOG_FILES(ID)
);

CREATE INDEX IDX_LOG_ENTRIES_EX_CLASS ON LOG_ENTRIES (EXCEPTION_CLASS);
CREATE INDEX IDX_LOG_ENTRIES_EX_TIME ON LOG_ENTRIES (EXCEPTION_TIME);
CREATE INDEX IDX_LOG_ENTRIES_SESSION ON LOG_ENTRIES (SESSION_ID);
CREATE INDEX IDX_LOG_ENTRIES_CALLBACK ON LOG_ENTRIES (CALLBACK_NAME);
CREATE INDEX IDX_LOG_ENTRIES_SEVERITY ON LOG_ENTRIES (SEVERITY);
CREATE INDEX IDX_LOG_ENTRIES_CATEGORY ON LOG_ENTRIES (ERROR_CATEGORY);
